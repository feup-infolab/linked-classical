package models;

public class StylePeriod {
    private String stylePeriodURI;
    private String title;
    private String stylePeriodUuid;

    public String getStylePeriodURI() {
        return stylePeriodURI;
    }

    public void setStylePeriodURI(String stylePeriodURI) {
        this.stylePeriodURI = stylePeriodURI;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStylePeriodUuid() {
        return stylePeriodUuid;
    }

    public void setStylePeriodUuid(String stylePeriodUuid) {
        this.stylePeriodUuid = stylePeriodUuid;
    }
}
